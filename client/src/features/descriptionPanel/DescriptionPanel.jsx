/**
 * Renders a panel displaying an AI-generated description.
 */

import { Box, Card, CardContent, Typography, CircularProgress } from '@mui/material'
import useDescription from './useDescription.jsx'
import geminiLogo from '../../../assets/gemini.png'

function DescriptionPanel() {
  const { description, loading } = useDescription()

  const boxStyle = {
    boxShadow: 1,
    borderRadius: 2,
    fontFamily: 'GoogleSans',
    fontSize: '.95rem',
    textAlign: 'center'
  }

  const cardStyle = {
    minHeight: 225, 
    width: 500, 
    justifySelf: 'center'
  }

  const geminiWrapperStyle = {
    display: 'flex', 
    flexDirection: 'row', 
    gap: 5,
    fontFamily: 'GoogleSans',
    color: 'darkgrey'
  }

  const headerStyle = {
    fontFamily: 'GoogleSans',
    fontWeight: 'bold',
    paddingBottom: 1,
  }

  return (
    <Box sx={boxStyle}>
      <Card variant='outlined'>
        <CardContent sx={cardStyle}>

          <div style={geminiWrapperStyle}>
            <Typography variant='h8'>
              Generated by
            </Typography>
            <img 
              src={geminiLogo}
              alt='Gemini'
              style={{ height: 15 }}
            />
          </div>

          <Typography variant='h6' sx={headerStyle}>
            Description
          </Typography>

          {loading ? <CircularProgress /> : description}

        </CardContent>
      </Card>
    </Box>
  )
}

export default DescriptionPanel